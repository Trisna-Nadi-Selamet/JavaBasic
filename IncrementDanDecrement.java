package JavaBasic;

public class IncrementDanDecrement {
    

    public static void main(String []args){
        
        //Operator
        
        //++a Pre-increment    nilai a ditambah satu,kemudian operasi berjalan
        //a++ post-increment   operasi dilaksanakan ,kemudian a ditambahkan
        //--a pre-drecrement   nilai a dikurang satu,kemudian operasi berjalan satu
        //a-- post-drecrement  operasi dilaksanakan ,kemudian a dikurangi satu
        
        int i = 1;

        System.out.println("i = "+ i);
        System.out.println("i = "+ ++i);
        System.out.println("i = "+ i++);
        System.out.println("i = "+ --i);
        System.out.println("i = "+ i--);
    
    
    }
}
